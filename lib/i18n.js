// Get translation for a specific key path
export function getTranslation(lang, keyPath) {
  const keys = keyPath.split('.');
  let value = translations[lang] || translations['EN'];
  
  for (const key of keys) {
    if (value && value[key] !== undefined) {
      value = value[key];
    } else {
      // Fallback to English
      let fallback = translations['EN'];
      for (const k of keys) {
        if (fallback && fallback[k] !== undefined) {
          fallback = fallback[k];
        } else {
          return keyPath; // Return the key path if not found
        }
      }
      return fallback;
    }
  }
  
  return value;
}

// Get language direction
export function getDirection(lang) {
  return languageMeta[lang]?.direction || 'ltr';
}

// RTL languages
const rtlLanguages = ['KU-BD', 'KU-SO', 'AR', 'FA'];

export function isRTL(lang) {
  return rtlLanguages.includes(lang);
}

// Format phone numbers always in LTR
export function formatPhoneLTR(phone) {
  return `\u202A${phone}\u202C`; // Unicode LTR override
}

